import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

import ms from "../index.js";
import { DELTA_SIDC_ICON_DATA } from "../src/delta/delta-sidc-icon-data.js";

const UNDEFINED_ICON_MARKER = "m 94.8206,78.1372 c -0.4542,6.8983";
const outputPath = path.join(
  path.dirname(fileURLToPath(import.meta.url)),
  "../src/delta/delta-missing-sidc-set.js"
);

function isMissingBaseSymbol(svg) {
  return String(svg || "").includes(UNDEFINED_ICON_MARKER);
}

const missingSidcs = [];

for (const sidc of Object.keys(DELTA_SIDC_ICON_DATA)) {
  const svg = new ms.Symbol(sidc).asSVG();
  if (isMissingBaseSymbol(svg)) {
    missingSidcs.push(sidc);
  }
}

missingSidcs.sort();

const fileContents =
  "// Auto-generated by dev/generate-delta-missing-sidc-set.mjs\n" +
  `// ${missingSidcs.length} SIDC(s) missing from base milsymbol as of generation time.\n` +
  "export const DELTA_MISSING_SIDC_SET = new Set([\n" +
  missingSidcs.map((sidc) => `  "${sidc}",`).join("\n") +
  "\n]);\n";

fs.writeFileSync(outputPath, fileContents);

console.log(
  `Wrote ${missingSidcs.length} missing SIDC(s) to ${path.relative(process.cwd(), outputPath)}`
);
